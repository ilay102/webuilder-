/**
 * text-packs.ts — Static Text Pool for Webuilder demo generation.
 *
 * Each pack is a complete, self-contained set of Hebrew copy + service
 * descriptions + testimonials + stats. Picking a pack at demo creation
 * time guarantees no text contamination between clients (no template
 * inheritance, no AI variance, no API latency).
 *
 * Lifecycle is managed by lib/text-pool-manager.ts (mirrors image pool).
 *
 * To add a new pack:
 *   1. Append to TEXT_PACKS below with a unique id
 *   2. Run `npm run text-pool:init` (idempotent — only registers new ids)
 *   3. Commit text-packs.ts + text-pool-state.json
 */

export interface TextCopy {
  h1:           string;   // Hero headline (4–8 words)
  heroSubtitle: string;   // Supporting subtitle (12–20 words)
  tagline:      string;   // Short brand tagline (≤ 8 words)
  about:        string;   // 2–3 sentence about paragraph
  ctaMain:      string;   // Primary CTA button (2–3 words)
  ctaSecondary: string;   // Secondary CTA
  sectionLabel: string;   // Section eyebrow label
}

export interface TextService {
  icon:  string;
  title: string;
  desc:  string;
}

export interface TextTestimonial {
  quote:  string;
  name:   string;
  detail: string;
}

export interface TextStat {
  value: string;
  label: string;
}

export interface TextPack {
  id:           string;
  vibe:         string;          // human label, debug only
  copy:         TextCopy;
  services:     TextService[];   // exactly 4 (matches dental template structure)
  testimonials: TextTestimonial[]; // exactly 3
  stats:        TextStat[];      // exactly 4
}

